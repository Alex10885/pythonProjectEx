
class Rectang(object):

    def __init__(self, x, y, width, height, speed):
        self.x = x
        self.y = y
        self.width = width
        self.height = height
        self.speed = speed


class Molecule(object):

    def __init__(self, xc, yc, radius, speed):
        self.xc = xc
        self.yc = yc
        self.radius = radius
        self.speed = speed

